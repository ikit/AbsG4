// Méthode pour switcher entre les différents menu
function switchRub(form1)
{
    var choice=form1.listRubriques.selectedIndex;

    // On supprime d'abord les anciennes options du second menu
    for (i = 0 ; i < form1.listSections.options.length ; i++)
    {
        form1.listSections.options[i] = null;
    }

    // On met ensuite à jours les options du second menu en accord avec l'option
    // choisie dans le premier menu
    switch(choice)
    {
        case 0: // Actions
            for (i = 0 ; i < liste0.length ; i++) {
            form1.listSections.options[i] = liste0[i];
            }
        break;
        case 1: // Emotions-Etat
            for (i = 0 ; i < liste1.length ; i++) {
            form1.listSections.options[i] = liste1[i];
            }
        break;
        case 2: // Personnages
            for (i = 0 ; i < liste2.length ; i++) {
            form1.listSections.options[i] = liste2[i];
            }
        break;
        case 3: // Divers
            for (i = 0 ; i < liste3.length ; i++) {
            form1.listSections.options[i] = liste3[i];
            }
        break;
        case 4: // Special
            for (i = 0 ; i < liste4.length ; i++) {
            form1.listSections.options[i] = liste4[i];
            }
        break;

    }
}


