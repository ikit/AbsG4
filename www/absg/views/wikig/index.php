
	<h1 class="wikig">Le wiki des G</h1>


	<p>Le wiki des G comme son nom l'indique est un wiki. C'est à dire une encyclopédie en ligne. Mais utilisable uniquement par les membres du site Absolument G. 
		L'idée est de mutualiser les connaissances des G.</p>
	
	<h2>Exemple :</h2>
	<p>Il nous est tous arrivé un jour l'envie d'écrire un article ou sur un bout de papier des choses qui, sur le moment au moins, nous semblaient intéressante à garder ou partager.
		Une recette de cuisine, la technique à mémé pour soigner le mal de tête, l'historique des remontés mécaniques de Lanslevillard, la galerie photo de la garde-robe d'un oncle, un exposé sur votre animal favori...
		Vous trouverez ici la place pour vous exprimer.</p>
	
	<h2>En somme :</h2>
	<p>On peut mettre ce qu'on veut dans ce wiki. Ca peut être sérieux ou décalé, utile ou non.</p>
	<p>Dans ce wiki tout le monde sera lecteur, rédacteur et modérateur. 
		Tout le monde pourra donc contribuer librement à l'encyclopédie, que ça soit en rédigeant de nouveau article ou en se contentant simplement de les lire ou de corriger les fautes d'orthographes ou la mise en page.</p>
	<p>A l'inverse du forum, les articles du wiki ne seront pas ordonné par date mais par thème.
		De plus il ne sera pas possible de discuter ou de commenter les articles... à moins de créer un sujet de dicussion pour ça dans le forum.</p>
	

