
	<h1 class="olympiages">Les OlympiaGes</h1>


	<p>Aaaaahhhhh ! Les OlympiaGes ! Voilà un projet probablement aussi vieux si ce n'est plus que le forum lui même. 
		Mais le problème c'est qu'il est pharaonique.</p>
	
	<h2>Kézako ?</h2>
	<p>Les OlympiaGes sera un ensemble de jeux, jouable uniquement sur internet. 
		Ces jeux ne seront accessible que par les membres du site Absolument G.
		Chacun aura ses statitiques, et il y aura des classements, des podiums, des compétition et des médailles !</p>
	
	<h2>Des délagations : </h2>
	<p>Comme pour les vrais jeux olympiques, un athlète défend un drapeau. 
		On parlera de délégation. Lors d'une édition des OlympiaGes, des délégations seront définis et les athlètes devront s'organiser pour défendre au mieux leurs couleurs.</p>
	<p>Ces délégations pourront varier d'une édition à l'autre. 
		Comme pour les AGPA où un thème particulier est décidé chaque année, avant de débuter une OlympiaGe, on devra déterminer les délégations. 
		Quelques exemples :
		<ul>
			<li>par Famille : Gueudelot, Guibert, Guyomard</li>
			<li>par sexe : les filles contre les gars ! Ouai !!!!</li>
			<li>par tirage au sort</li>
			<li>par couleur de cheveux... par ce qu'on veut en fait...</li>
		</ul>
	</p>

	<h2>Des jeux : </h2>
	<p>Les jeux des OlympiaGes seront essentiellement des jeux type "question réponse" à la trivial pursuit. Sauf qu'en plus des thèmes traditionnels (histoire, science, etc) il y aura le thème famille.
		De même les données du site (citations, image du moment, agenda, culture G, etc) seront utilisé dans les minis-jeux.</p>

	<h2>Conclusion </h2>
	<p>Un beau projet... mais très long à mettre en place...</p>

