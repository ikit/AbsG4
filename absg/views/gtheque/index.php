

	<h1 class="gtheque">G-thèque</h1>


	<p>La G-thèque offre la possibilité aux G se tenir en ligne des thèque et de lister leur contenu. Ces thèques pourront être de plusieurs types :</p>
		<ul>
			<li>Bibliothèque : pour les livres</li>
			<li>Discothèque : pour les musiques</li>
			<li>Vidéothèque : pour les vidéos en tout genre du grand ou du petit écran</li>
			<li>Oeunothèque : pour la cave</li>
			<li>Ludothèque : pour les jeux vidéos ou de plateau</li>
		</ul>
	<p>Il sera possible si vous avez d'autres idées/besoins de rajouter d'autres type de thèque.</p>
	
	
	<h2>Utilité :</h2>
	<p>Prenons l'exemple de la bibliothèque. Pour ceux comme Olivier qui y attache une grande importance, ça permet de gérer ses collections, tenir ses "comptes" à jour, et avoir une vue d'ensemble exaustive accessible partout.
		Ca permet aussi aux autres G de voir la bibliothèque d'Olivier. Les G ayant ainsi connaissance des oeuvres des autres, la recherche, l'échange et le prêt sera facilité. 
		(Bon reste le problème de la distance, mais la Gposte prévu pour 3055 permettra d'échanger facilement et concretement les objets).
		Un autre intéret concerne la période des fêtes. Si on est en panne d'idée pour trouver un cadeau, un rapide coup d'oeil aux thèques de l'impétrant permettra de trouver à coup sûr un cadeau qu'il n'a pas et qui lui fera plaisir.</p>
	
	<h2>Fonctionnement :</h2>
	<p>Chaque G pourra gérer une thèque par type. Il sera aussi possible pour les couples de gérer des thèques en commun. L'enregistrement complet d'un objet ne sera à faire qu'une fois.
		Prenons toujours l'exemple de la bibliothèque. Pour enregistrer une nouvelle BD il faudra saisir un certains nombre d'information (titre, auteurs, collection, tome, edition, format, couverture). 
		Une fois qu'un premier G a entré ces infos, elles sont en base. Si un autre G se procure à son tour cet ouvrage, la fiche sera déjà crée. il n'aura plus qu'à dire qu'il possède aussi cet oeuvre et c'est tout.
		Il faudra donc penser à vérifier que l'objet n'existe pas déjà en base avant de créer une fiche. Mais rassurez vous, l'informatique vous aidera efficacement pour ça.</p>
	<p>A noter que les thèques ne concernent que les objets réels. Ainsi les bibliothèques numériques, les Gigas de musiques ou de vidéos sur disque dur ne sont à priori pas concerné.
		Si vous en faites la demande ça peut changer.</p>
	


